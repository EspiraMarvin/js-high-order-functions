//          JAVASCRIPT ARRAY METHODS

//    pop array method - removes the last element of an array, and returns that element
// This method changes the length of an array
// To remove the first element of an array, use the shift() method

const numbers = [1,2,3];

const lastItem =  numbers.pop();

console.log(numbers); // removes last element in an array
console.log(lastItem); // and returns it
